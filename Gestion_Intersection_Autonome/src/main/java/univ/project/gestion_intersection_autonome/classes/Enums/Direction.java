package univ.project.gestion_intersection_autonome.classes.Enums;

/**
 * Enumération représentant les directions possibles pour les véhicules dans le système.
 * Chaque direction est définie comme une constante.
 *
 * Les directions possibles sont :
 * - NORD : Direction vers le nord.
 * - SUD : Direction vers le sud.
 * - EST : Direction vers l'est.
 * - OUEST : Direction vers l'ouest.
 * - NORDOUEST : Direction vers le nord-ouest.
 * - NORDEST : Direction vers le nord-est.
 * - SUDOUEST : Direction vers le sud-ouest.
 * - SUDEST : Direction vers le sud-est.
 */

public enum Direction {

    NORD,

    SUD,

    EST,

    OUEST,

    NORDOUEST,

    NORDEST,

    SUDOUEST,

    SUDEST
}